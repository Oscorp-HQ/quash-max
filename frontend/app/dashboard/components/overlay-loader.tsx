"use client";

import * as React from "react";
import * as AlertDialogPrimitive from "@radix-ui/react-alert-dialog";

import { cn } from "@/lib/utils";
import { buttonVariants } from "@/components/ui/button";

const LoaderDialog = AlertDialogPrimitive.Root;

const LoaderDialogTrigger = AlertDialogPrimitive.Trigger;

interface LoaderDialogPortalProps
  extends AlertDialogPrimitive.AlertDialogPortalProps {
  className?: string;
}

const LoaderDialogPortal = ({
  className,
  children,
  ...props
}: LoaderDialogPortalProps) => (
  <AlertDialogPrimitive.Portal {...props}>
    <div className={cn(className)}>{children}</div>
  </AlertDialogPrimitive.Portal>
);
LoaderDialogPortal.displayName = AlertDialogPrimitive.Portal.displayName;

const LoaderDialogOverlay = React.forwardRef<
  React.ElementRef<typeof AlertDialogPrimitive.Overlay>,
  React.ComponentPropsWithoutRef<typeof AlertDialogPrimitive.Overlay>
>(({ className, children, ...props }, ref) => (
  <AlertDialogPrimitive.Overlay
    className={cn(
      "fixed inset-0 z-50 bg-background/10 backdrop-blur-sm data-[state=open]:animate-in data-[state=closed]:animate-out data-[state=closed]:fade-out-0 data-[state=open]:fade-in-0",
      className,
    )}
    {...props}
    ref={ref}
  />
));
LoaderDialogOverlay.displayName = AlertDialogPrimitive.Overlay.displayName;

const LoaderDialogContent = React.forwardRef<
  React.ElementRef<typeof AlertDialogPrimitive.Content>,
  React.ComponentPropsWithoutRef<typeof AlertDialogPrimitive.Content>
>(({ className, ...props }, ref) => (
  <LoaderDialogPortal>
    <LoaderDialogOverlay />
    <AlertDialogPrimitive.Content
      ref={ref}
      className={cn(
        "fixed left-[50%] top-[50%] z-50 grid w-full max-w-lg translate-x-[-50%] translate-y-[-50%] gap-4 p-6  duration-200 data-[state=open]:animate-in data-[state=closed]:animate-out data-[state=closed]:fade-out-0 data-[state=open]:fade-in-0 data-[state=closed]:zoom-out-95 data-[state=open]:zoom-in-95 data-[state=closed]:slide-out-to-left-1/2 data-[state=closed]:slide-out-to-top-[48%] data-[state=open]:slide-in-from-left-1/2 data-[state=open]:slide-in-from-top-[48%] sm:rounded-lg md:w-full",
        className,
      )}
      {...props}
    />
  </LoaderDialogPortal>
));
LoaderDialogContent.displayName = AlertDialogPrimitive.Content.displayName;

const LoaderDialogHeader = ({
  className,
  ...props
}: React.HTMLAttributes<HTMLDivElement>) => (
  <div
    className={cn(
      "flex flex-col space-y-2 text-center sm:text-left",
      className,
    )}
    {...props}
  />
);
LoaderDialogHeader.displayName = "AlertDialogHeader";

const LoaderDialogFooter = ({
  className,
  ...props
}: React.HTMLAttributes<HTMLDivElement>) => (
  <div
    className={cn(
      "flex flex-col-reverse sm:flex-row sm:justify-end sm:space-x-2",
      className,
    )}
    {...props}
  />
);
LoaderDialogFooter.displayName = "AlertDialogFooter";

const LoaderDialogTitle = React.forwardRef<
  React.ElementRef<typeof AlertDialogPrimitive.Title>,
  React.ComponentPropsWithoutRef<typeof AlertDialogPrimitive.Title>
>(({ className, ...props }, ref) => (
  <AlertDialogPrimitive.Title
    ref={ref}
    className={cn("text-lg font-semibold", className)}
    {...props}
  />
));
LoaderDialogTitle.displayName = AlertDialogPrimitive.Title.displayName;

const LoaderDialogDescription = React.forwardRef<
  React.ElementRef<typeof AlertDialogPrimitive.Description>,
  React.ComponentPropsWithoutRef<typeof AlertDialogPrimitive.Description>
>(({ className, ...props }, ref) => (
  <AlertDialogPrimitive.Description
    ref={ref}
    className={cn("text-sm text-muted-foreground", className)}
    {...props}
  />
));
LoaderDialogDescription.displayName =
  AlertDialogPrimitive.Description.displayName;

const LoaderDialogAction = React.forwardRef<
  React.ElementRef<typeof AlertDialogPrimitive.Action>,
  React.ComponentPropsWithoutRef<typeof AlertDialogPrimitive.Action>
>(({ className, ...props }, ref) => (
  <AlertDialogPrimitive.Action
    ref={ref}
    className={cn(buttonVariants(), className)}
    {...props}
  />
));
LoaderDialogAction.displayName = AlertDialogPrimitive.Action.displayName;

const LoaderDialogCancel = React.forwardRef<
  React.ElementRef<typeof AlertDialogPrimitive.Cancel>,
  React.ComponentPropsWithoutRef<typeof AlertDialogPrimitive.Cancel>
>(({ className, ...props }, ref) => (
  <AlertDialogPrimitive.Cancel
    ref={ref}
    className={cn(
      buttonVariants({ variant: "outline" }),
      "mt-2 sm:mt-0",
      className,
    )}
    {...props}
  />
));
LoaderDialogCancel.displayName = AlertDialogPrimitive.Cancel.displayName;

export {
  LoaderDialog,
  LoaderDialogTrigger,
  LoaderDialogContent,
  LoaderDialogHeader,
  LoaderDialogFooter,
  LoaderDialogTitle,
  LoaderDialogDescription,
  LoaderDialogAction,
  LoaderDialogCancel,
};
